package org.fade.demo.jvm.basic.instructions;

/**
 * 方法返回指令示例
 * @author fade
 */
public class MethodReturnExample {

    public boolean booleanReturn() {
        return false;
    }

    public byte byteReturn() {
        return 1;
    }

    public char charReturn() {
        return 'a';
    }

    public short shortReturn() {
        return 1;
    }

    public int intReturn() {
        return 1;
    }

    public long longReturn() {
        return 1;
    }

    public float floatReturn() {
        return 1.f;
    }

    public double doubleReturn() {
        return 1.;
    }

    public String referenceReturn() {
        return "hello";
    }

    public void voidReturn() {

    }

}
